// 1. Date Object
const newDate = new Date();
console.log(typeof newDate); // object

console.log(newDate); // current date and time
console.log(newDate.toString()); // current date and time
console.log(newDate.toDateString()); // current date
console.log(newDate.toTimeString()); // current time
console.log(newDate.toISOString()); // current date and time in ISO format
console.log(newDate.toJSON()); // current date and time in JSON format
console.log(newDate.toLocaleString()); // current date and time in local format





